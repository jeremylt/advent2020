#[macro_use]
extern crate lazy_static;
use colored::*;

// -----------------------------------------------------------------------------
// Modules
// -----------------------------------------------------------------------------
mod day01;
mod day02;

// -----------------------------------------------------------------------------
// Main Driver
// -----------------------------------------------------------------------------
fn main() {
    println!("{}", "-------------------------------".green().bold());
    println!(
        "{} {} {}",
        "-----".red().bold(),
        "Advent of Code 2020",
        "-----".red().bold()
    );
    println!("{}", "-------------------------------".green().bold());
    day01::run();
    println!("{}", "-------------------------------".green().bold());
    day02::run();
    println!("{}", "-------------------------------".green().bold());
}

// -----------------------------------------------------------------------------
// Tests
// -----------------------------------------------------------------------------
#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test01() {
        let results = day01::run();
        assert_eq!(results.0, 326211); // Part 1
        assert_eq!(results.1, 131347190); // Part 2
        assert!(results.2 < 250); // Timing
    }

    #[test]
    fn test02() {
        let results = day02::run();
        assert_eq!(results.0, 538); // Part 1
        assert_eq!(results.1, 489); // Part 2
        assert!(results.2 < 250); // Timing
    }
}

